function transform2KeyPosition(
  data: EmojiData[],
): { [key in string]: EmojiData['position'] } {
  let usedKeys = ['cn', 'us', 'code', 'web_code']
  let res: { [key in string]: EmojiData['position'] } = {}
  data.forEach((item: EmojiData) => {
    usedKeys.forEach((key: keyof Omit<EmojiData, 'id' | 'position'>) => {
      let trieKey = item[key]
      if (trieKey && trieKey !== '[]' && !(trieKey in res)) {
        res[trieKey] = item.position
      }
    })
  })
  return res
}

